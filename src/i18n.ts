export function t(language: Language, key: string): string {
  if (language === "zh") return zh[key] ?? en[key] ?? key;
  if (language === "bilingual") {
    const english = en[key] ?? key;
    const chinese = zh[key];
    return chinese ? `${english} / ${chinese}` : english;
  }
  return en[key] ?? key;
}

